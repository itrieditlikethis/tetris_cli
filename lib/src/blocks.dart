// Generate random block
int _getRandom(int min, int max) {
  return Random().nextInt(max - min + 1) + min;
}

// Block types
typedef BlockType = int;
// 0 - square
// 1 - line
// 2 - L
// 3 - mirror L
// 4 - N
// 5 - mirror N
// 6 - T

const List<BlockType> _blockTypes = [0, 1, 2, 3, 4, 5, 6];

BlockType _getRandomBlockType() =>
    _blockTypes[_getRandom(0, _blockTypes.length - 1)];

// Block turn types
typedef BlockRotation = int;
// 0 - zero
// 1 - one
// 2 - two
// 3 - three

// Get random figure
const List<BlockRotation> _blockRotations = [0, 1, 2, 3];
BlockRotation _getRandomRotation() =>
    _blockRotations[_getRandom(0, _blockRotations.length - 1)];

